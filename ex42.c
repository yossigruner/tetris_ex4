#define MAP_WIDTH 20
#define MAP_HEIGHT 20

#define EMPTY_CHAR ' '
#define TIMER_TIME 1
#define MAP_EGED '*'
#define BRICK_CHAR '-'

int brickCurrentPositionX;
int brickCurrentPositionY;
int isVertical = 1;
char map[MAP_WIDTH][MAP_HEIGHT];

void clearAndPrintLogo();

void handleSignal(int sigint) {
	char selectedChar = 0;
	signal(SIGUSR2, handleSignal);
	if (sigint == SIGUSR2) {
		read(STDIN_FILENO, &selectedChar, 1);
		switch (selectedChar) {
		case 'a':
			doLeft();
			break;
		case 'd':
			doRight();
			break;
		case 'w':
			doRotate();
			break;
		case 's':
			updateBrickLocation();
			break;
		}
	}
}

void init() {
	// init map
	int i, j;
	for (i = 0; i < MAP_WIDTH; i++) {
		for (j = 0; j < MAP_HEIGHT; j++) {
			if (i == 0 || j == 0 || i == MAP_HEIGHT - 1
					|| j == MAP_HEIGHT - 1) {
				map[i][j] = MAP_EGED;
				if ((j > 0) && (j < 19) && (i == 0))
					map[i][j] = EMPTY_CHAR;

			} else {
				map[i][j] = EMPTY_CHAR;
			}
		}
	}

	// set start position
	brickCurrentPositionX = 2;
	brickCurrentPositionY = 10;
}

void updateMap()
{
	clearLinesInNeeded();

	int i, j;
	for (i = 0; i < MAP_WIDTH; i++) {
		for (j = 0; j < MAP_HEIGHT; j++) {
			putchar(map[i][j]);
		}
		putchar('\n');
	}
}

void updateBrickLocation()
{
	if (isVertical == 0) {
		if (brickCurrentPositionX < 19 && canMove(map) == 0) {
			deleteBrick(map);
			brickCurrentPositionX++;
			printBrick(map);
		} else {
			isVertical = rand() % 2;
			brickCurrentPositionX = (isVertical == 0) ? 1 : 2;
			brickCurrentPositionY = 10;
			printBrick(map);
		}
	} else {
		if (brickCurrentPositionX < 18 && canMove(map) == 0) {
			deleteBrick(map);
			brickCurrentPositionX++;
			printBrick(map);
		} else {
			isVertical = rand() % 2;
			brickCurrentPositionX = (isVertical == 0) ? 1 : 2;
			brickCurrentPositionY = 10;
			printBrick(map);
		}
	}

}

void doRotate()
{
	deleteBrick(map);
	if (isVertical == 0) {
		if (brickCurrentPositionX + 1 < 18&& map[brickCurrentPositionX + 1][brickCurrentPositionY] == EMPTY_CHAR
		&& map[brickCurrentPositionX - 1][brickCurrentPositionY] == EMPTY_CHAR) {
			isVertical = 1;
		}
	} else {
		if (brickCurrentPositionY - 1 > 2&& brickCurrentPositionY + 1 < 18 && map[brickCurrentPositionX][brickCurrentPositionY - 1] == EMPTY_CHAR
		&& map[brickCurrentPositionX][brickCurrentPositionY + 1] == EMPTY_CHAR) {
			isVertical = 0;
		}
	}
	printBrick(map);
	return;
}

void deleteBrick()
{
	if (isVertical == 0) {
		map[brickCurrentPositionX][brickCurrentPositionY] = EMPTY_CHAR;
		map[brickCurrentPositionX][brickCurrentPositionY + 1] = EMPTY_CHAR;
		map[brickCurrentPositionX][brickCurrentPositionY - 1] = EMPTY_CHAR;
	} else {
		map[brickCurrentPositionX][brickCurrentPositionY] = EMPTY_CHAR;
		map[brickCurrentPositionX - 1][brickCurrentPositionY] = EMPTY_CHAR;
		map[brickCurrentPositionX + 1][brickCurrentPositionY] = EMPTY_CHAR;
	}
}

void printBrick()
{
	if (isVertical == 0)
			{
		map[brickCurrentPositionX][brickCurrentPositionY] = BRICK_CHAR;
		map[brickCurrentPositionX][brickCurrentPositionY + 1] = BRICK_CHAR;
		map[brickCurrentPositionX][brickCurrentPositionY - 1] = BRICK_CHAR;
	} else
	{
		map[brickCurrentPositionX][brickCurrentPositionY] = BRICK_CHAR;
		map[brickCurrentPositionX - 1][brickCurrentPositionY] = BRICK_CHAR;
		map[brickCurrentPositionX + 1][brickCurrentPositionY] = BRICK_CHAR;
	}
}

int canMove()
{
	if (isVertical == 0) {
		if (map[brickCurrentPositionX + 1][brickCurrentPositionY] != EMPTY_CHAR
				|| map[brickCurrentPositionX + 1][brickCurrentPositionY + 1] != EMPTY_CHAR
				|| map[brickCurrentPositionX + 1][brickCurrentPositionY - 1] != EMPTY_CHAR) {
			return 1;
		} else {
			return 0;
		}
	} else {
		if (map[brickCurrentPositionX + 2][brickCurrentPositionY] != EMPTY_CHAR) {
			return 1;
		} else {
			return 0;
		}
	}
}

void doRight() {
	deleteBrick();
	if (isVertical == 0) {
		if (brickCurrentPositionY + 1 < 18&& map[brickCurrentPositionX][brickCurrentPositionY + 1] == EMPTY_CHAR
		&& map[brickCurrentPositionX][brickCurrentPositionY + 2] == EMPTY_CHAR && map[brickCurrentPositionX][brickCurrentPositionY] == EMPTY_CHAR) {
			brickCurrentPositionY++;
		}
	} else {
		if (brickCurrentPositionY < 18&& map[brickCurrentPositionX][brickCurrentPositionY + 1] == EMPTY_CHAR
		&& map[brickCurrentPositionX + 1][brickCurrentPositionY + 1] == EMPTY_CHAR
		&& map[brickCurrentPositionX - 1][brickCurrentPositionY + 1] == EMPTY_CHAR) {
			brickCurrentPositionY++;
		}
	}
	printBrick();
}

void doLeft()
{
	deleteBrick();
	if (isVertical == 0) {
		if (brickCurrentPositionY - 1 > 2 && map[brickCurrentPositionX][brickCurrentPositionY - 1] == EMPTY_CHAR
				&& map[brickCurrentPositionX][brickCurrentPositionY - 2] == EMPTY_CHAR && map[brickCurrentPositionX][brickCurrentPositionY] == EMPTY_CHAR) {
			brickCurrentPositionY--;
		}
	} else {
		if (brickCurrentPositionY > 1 && map[brickCurrentPositionX][brickCurrentPositionY - 1] == EMPTY_CHAR
				&& map[brickCurrentPositionX + 1][brickCurrentPositionY - 1] == EMPTY_CHAR
				&& map[brickCurrentPositionX - 1][brickCurrentPositionY - 1] == EMPTY_CHAR) {
			brickCurrentPositionY--;
		}
	}
	printBrick();
}

void clearLinesInNeeded() {
    int i, j, x, y, cleard;
    for (i = MAP_WIDTH - 2; i >= 0; i--) {
        cleard = 1;
        for (j = 1; j < MAP_WIDTH - 1; j++) {
            // check if there's a space in line
            if (map[i][j] == EMPTY_CHAR) {
                cleard = 0;
            }
        }
        if (cleard) {  // no spaces in line
            // clear line
            for (j = 1; j < MAP_WIDTH - 1; j++) {
                map[i][j] = EMPTY_CHAR;
            }

            // move other lines down
            for (x = i; x > 0; x--) {
                for (y = 1; y < MAP_WIDTH - 1; y++) {
                    map[x][y] = map[x - 1][y];
                }
            }
            i++;  // since a complete line can fall down to the same location we should check it
                  // again
        }
    }
}

int main() {
	// register for signal
	signal(SIGUSR2, handleSignal);
	int i = 0;
	srand(time(NULL));

	// init map
	init();
	// update initial stae
	updateMap(map);

	clearAndPrintLogo();
	while (1) {
		sleep(1);
		clearAndPrintLogo();
		updateBrickLocation();
		updateMap();
	}

	return EXIT_SUCCESS;
}

